// Mapeo de archivos PDF por idioma
const pdfFiles = {
    'es': './pdf/catalogo-salmoprime-es.pdf',
    'pt': './pdf/catalogo-salmoprime-pt.pdf',
    'en': './pdf/catalogo-salmoprime-en.pdf'
};

// Función para cambiar idioma
function changeLanguage(lang) {
    // Actualizar el atributo lang del documento
    document.documentElement.lang = lang;
    
    // Actualizar botones de idioma activos
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });
    
    // Aplicar traducciones a todos los elementos con data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang]?.[key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Actualizar PDF según idioma
    updatePdfForLanguage(lang);
    
    // Guardar preferencia de idioma
    localStorage.setItem('preferred-language', lang);
}

// Función para actualizar PDF según idioma
function updatePdfForLanguage(lang) {
    const pdfUrl = pdfFiles[lang];
    
    // Actualizar iframe del visor de PDF
    const pdfViewer = document.getElementById('pdfViewer');
    if (pdfViewer) {
        pdfViewer.src = pdfUrl;
    }
    
    // Actualizar enlaces de descarga
    const downloadLinks = document.querySelectorAll('[data-pdf-download]');
    downloadLinks.forEach(link => {
        link.href = pdfUrl;
        link.download = getPdfFileName(lang);
    });
    
    // Actualizar enlace alternativo
    const alternativeLink = document.querySelector('.pdf-alternative a');
    if (alternativeLink) {
        alternativeLink.href = pdfUrl;
        alternativeLink.download = getPdfFileName(lang);
    }
}

// Función para obtener el nombre del archivo PDF según idioma
function getPdfFileName(lang) {
    const fileNames = {
        'es': 'catalogo-salmoprime-es.pdf',
        'pt': 'catalogo-salmoprime-pt.pdf',
        'en': 'catalogo-salmoprime-en.pdf'
    };
    return fileNames[lang];
}

// MEJORAS PARA BOTONES - Efectos y Estados
function initButtonEnhancements() {
    // Efecto de ripple para botones de filtro
    const filterButtons = document.querySelectorAll('.filter-btn');
    
    filterButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            // Efecto de pulso al hacer clic
            this.style.transform = 'scale(0.95)';
            setTimeout(() => {
                this.style.transform = '';
            }, 150);
        });
    });

    // Efecto de carga para botón de pantalla completa
    const fullscreenButton = document.getElementById('fullscreenToggle');
    if (fullscreenButton) {
        fullscreenButton.addEventListener('click', function() {
            this.classList.add('btn-loading');
            setTimeout(() => {
                this.classList.remove('btn-loading');
            }, 600);
        });
    }

    // Efecto hover mejorado para botones de descarga
    const downloadButtons = document.querySelectorAll('.pdf-download-link');
    downloadButtons.forEach(button => {
        button.addEventListener('mouseenter', function() {
            const icon = this.querySelector('.download-icon');
            if (icon) {
                icon.style.transform = 'translateY(2px) scale(1.1)';
            }
        });
        
        button.addEventListener('mouseleave', function() {
            const icon = this.querySelector('.download-icon');
            if (icon) {
                icon.style.transform = '';
            }
        });
    });
}

